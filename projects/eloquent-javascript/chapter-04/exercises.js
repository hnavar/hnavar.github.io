function range(num1, num2) {
  let rangeArray = [];
  if (num1 > num2) {
    for(let i = num2 ; i <= num1; i++) {
    rangeArray.push(i);  
    } return rangeArray;
  } 
  if (num2 > num1) {
    for(let i = num1 ; i <= num2; i++) {
      rangeArray.push(i);
    }
  } return rangeArray;
}

