// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(num1, num2) {
  for (var i = num1; i <= num2; i++) {
    if (i % 3 === 0 & i % 5 === 0) {
        console.log("fizzbuzz");
    } else if (i % 5 === 0) {
        console.log("buzz");
    } else if (i % 3 === 0) {
       console.log("fizz");
    } else {
        console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////



function drawChessboard (size) {
  var board = [];
  for (let i = 0; i < size; i++) {
    for (let j = 0; j < size; j++) { 
      if ((i + j) % 2 == 0) {
        board += " ";
      } else {
        board += "#";
      }
    } board += "\n";
  } console.log(board);
}

