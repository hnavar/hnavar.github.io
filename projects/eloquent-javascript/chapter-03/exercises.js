// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num === 0) {
    return true;
  } else if (num % 2 === 1 || num % 2 === -1 ) {
    return false;
  } else {
  return isEven(num - 2);
  }
}
    




