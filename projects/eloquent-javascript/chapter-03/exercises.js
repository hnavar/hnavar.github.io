// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var count = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) {
      count = count + 1;
    }
  } return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
 var count = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === 'b' || string[i] === 'B') {
      count = count + 1;
    }
  } return count;
}


